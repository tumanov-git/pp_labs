"""
Иерархия исключений для системы управления курортом.
"""


class ResortError(Exception):
    """Базовое исключение для всех ошибок системы курорта."""
    pass


class EntityNotFoundError(ResortError):
    """Исключение, возникающее когда запрашиваемая сущность не найдена.
    
    Используется в CRUD-операциях, когда пытаются получить, обновить
    или удалить сущность по несуществующему ID.
    """
    pass


class ValidationError(ResortError):
    """Исключение, возникающее при невалидных входных данных.
    
    Используется при проверке корректности данных, например:
    - отрицательные цены или количества
    - отрицательная вместимость
    - некорректные даты
    """
    pass


class StorageError(ResortError):
    """Исключение, возникающее при ошибках работы с хранилищем.
    
    Используется при ошибках чтения/записи файлов (JSON/XML),
    проблемах с парсингом данных или других операциях ввода-вывода.
    """
    pass

